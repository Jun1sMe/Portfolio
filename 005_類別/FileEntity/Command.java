
public class Command
{
	// 定義指令名稱，並設為常數
	public static final int NONE = 0;	// 空指令
	public static final int CD = 1;		// 進入目錄夾
	public static final int BACK = 2;	// 退回上層
	public static final int TOUCH = 3;	// 建立新檔
	public static final int MKDIR = 4;	// 建立新目錄夾
	public static final int LS = 5;		// 列出目錄夾中所有檔案
	public static final int SEARCH = 6;	// 搜尋
}
